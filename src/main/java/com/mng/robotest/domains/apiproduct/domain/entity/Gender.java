package com.mng.robotest.domains.apiproduct.domain.entity;

public class Gender {
	
	String id;
	String name;
	String genderRelation;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGenderRelation() {
		return genderRelation;
	}
	public void setGenderRelation(String genderRelation) {
		this.genderRelation = genderRelation;
	}
	
}
